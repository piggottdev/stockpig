/**
 * Piece type provides an enumeration of all piece types including the empty piece.
 */
public final class PieceType {

    public static final byte
            EMPTY = 0, KING = 1, PAWN = 2, KNIGHT = 3, BISHOP = 4, ROOK = 5, QUEEN = 6;


    private PieceType() {}
}
